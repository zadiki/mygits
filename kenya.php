<h3 class="red">The education system in KENYA</h3><p class="leading">


The kenyan education system is eight years in primary school, four years in high school and four years in tertiary institution . 8-4-4</p><p class="leading">
The high school examination is carried out by the kenyan Examination Council,KNEC.</p><p class="leading">
Exam is composed of a maximum of eight subjects. Seven subjects are also allowed.</p><p class="leading">
The exams has compulsory subjects and optional subjects.The compulsory subjects are; English, kiswahili, and Mathematics.<br>
Also the student has to choose 2-3 Science subjects.<br>
He/she can either choose biology/chemistry &phyics or take all the three together if possible.<br>
He/she is also supposed to take to art courses.<br>
Thus the kenyan education system entails a general combination for educational backgrounds and thus has gained recognition all over tha world as an equivalent exams .<br>
<h3 class="red">Market overview.</h3>
Altogether, there are about 100 universities in East Africa, both public and private, with Kenya hosting the highest number, 40.<br>
With local Kenyan universities not being able to offer enough places for students, more students are looking into opportunities
 to study overseas as they also wish to improve their career prospects in the labor market after graduation.<br>
According to University World News, 57% of 1,044 Kenyan students polled in the recent survey said that they would prefer to study 
in a foreign university than a local one. The number of Kenyans issued with student visas has been on the rise in recent years 
according to government statistics.for Instance Turkey has lifted the Visa fees for students willing to study in turkey.<br>

In the past decade Tanzania's public universities have witnessed rapid growth in enrolment. There are nearly 30 public and private universities in Tanzania. Demand for higher education is large with an estimated student population of 15,000.<br>
According to a recent report from the Tanzania Education Sector Analysis, emphasis has been put on higher education, to adequately meet the growing demand from secondary school leavers and produce skills relevant to current and future economic growth. University enrolment has grown at an average annual rate of 30% over 2005-09.<br>
Thus the EAC universities can not cater for all thier graduate. Thus the external universities  have easy catch for students.<br>
Also many companies prefer to employ graduates from abroad on the thinking that they have aquired a higher level of education than the local graduates<br>

